W, H, ADV = 5, 7, 6


def row_byte(row):
    """5-bit row -> 0..255 byte, leftmost column in the MSB (bit 7)."""
    v = 0
    for i, ch in enumerate(row):
        if ch == "#":
            v |= 1 << (7 - i)
    return v


def main():
    out = []
    out.append("STARTFONT 2.1")
    out.append("FONT -sjqtentacles-font5x7-medium-r-normal--7-70-75-75-c-50-iso10646-1")
    out.append("SIZE 7 75 75")
    out.append(f"FONTBOUNDINGBOX {W} {H} 0 0")
    out.append("STARTPROPERTIES 5")
    out.append("FONT_ASCENT 7")
    out.append("FONT_DESCENT 0")
    out.append("DEFAULT_CHAR 63")
    out.append(f"PIXEL_SIZE {H}")
    out.append("POINT_SIZE 70")
    out.append("ENDPROPERTIES")
    out.append(f"CHARS {len(GLYPHS)}")
    for ch, rows in GLYPHS:
        assert len(rows) == H and all(len(r) == W for r in rows), ch
        code = ord(ch)
        name = "space" if ch == " " else f"char{code}"
        out.append(f"STARTCHAR {name}")
        out.append(f"ENCODING {code}")
        out.append("SWIDTH 480 0")
        out.append(f"DWIDTH {ADV} 0")
        out.append(f"BBX {W} {H} 0 0")
        out.append("BITMAP")
        for r in rows:
            out.append(f"{row_byte(r):02X}")
        out.append("ENDCHAR")
    out.append("ENDFONT")
    text = "\n".join(out) + "\n"
    with open("data/font5x7.bdf", "w") as f:
        f.write(text)
    print(f"wrote data/font5x7.bdf ({len(GLYPHS)} glyphs)")


if __name__ == "__main__":
    main()
